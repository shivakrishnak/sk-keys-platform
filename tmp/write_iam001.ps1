$lines.Add("")
$lines.Add("**CORE INVARIANTS:**")
$lines.Add("")
$lines.Add("1. A digital system cannot physically observe who is")
$lines.Add("   interacting with it - it can only verify claims.")
$lines.Add("   Every identity is a CLAIM that must be proven.")
$lines.Add("")
