$lines.Add("### `u{1f6a8} Failure Modes & Diagnosis")
$lines.Add("")
$lines.Add("**Broken access control**")
$lines.Add("")
$lines.Add("**Symptom:** User A accesses User B's data. Admin")
$lines.Add("endpoints return 200 without admin credentials.")
$lines.Add("")
$lines.Add("**Root Cause:** Authorization logic was never applied")
$lines.Add("to the endpoint, checks the wrong identifier, or has")
$lines.Add("a logic bug (OR instead of AND in permission check).")
$lines.Add("")
$lines.Add("**Diagnostic:**")
$lines.Add("")
$lines.Add("``````bash")
$lines.Add("# Test endpoint with wrong user's token")
$lines.Add("curl -H ""Authorization: Bearer {user_a_token}"" \")
$lines.Add("  https://api.example.com/users/user_b/data")
$lines.Add("# Expected: 403. Actual 200 = broken.")
$lines.Add("")
$lines.Add("# Check auth middleware applied to all routes")
$lines.Add("grep -r ""requiresAuth\|@PreAuthorize\|authorize"" \")
$lines.Add("  src/controllers/ | wc -l")
$lines.Add("# Compare to total number of controller files/routes")
$lines.Add("``````")
$lines.Add("")
$lines.Add("**Fix:** Default-deny policy. Every endpoint explicitly")
$lines.Add("declares required permissions. Test authorization in CI.")
$lines.Add("")
$lines.Add("**Prevention:** Centralized authorization middleware.")
$lines.Add("Never rely on route-level filtering alone.")
$lines.Add("")
$lines.Add("---")
$lines.Add("")
$lines.Add("**Token not invalidated after logout**")
$lines.Add("")
$lines.Add("**Symptom:** Logged-out user's token still works.")
$lines.Add("Stolen tokens remain usable until natural expiry.")
$lines.Add("")
$lines.Add("**Root Cause:** JWTs are stateless - no server record")
$lines.Add("to invalidate. Token remains valid until ``exp`` claim.")
$lines.Add("")
$lines.Add("**Diagnostic:**")
$lines.Add("")
$lines.Add("``````bash")
$lines.Add("# Decode JWT payload (no signature validation)")
$lines.Add("echo ""{token}"" | cut -d. -f2 | \")
$lines.Add("  base64 -d 2>/dev/null | python3 -m json.tool")
$lines.Add("# Check exp field - how far in the future?")
$lines.Add("")
$lines.Add("# Test if token works post-logout")
$lines.Add("curl -H ""Authorization: Bearer {old_token}"" \")
$lines.Add("  https://api.example.com/me")
$lines.Add("# 200 after logout = token not invalidated")
$lines.Add("``````")
$lines.Add("")
$lines.Add("**Fix:** Short-lived access tokens (15 min) + revoke")
$lines.Add("refresh token on logout. Or opaque tokens with server-")
$lines.Add("side session store enabling instant revocation.")
$lines.Add("")
$lines.Add("**Prevention:** Design token lifetime based on your")
$lines.Add("revocation latency tolerance at system design time.")
$lines.Add("")
