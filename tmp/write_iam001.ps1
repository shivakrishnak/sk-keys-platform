$lines.Add("### `u{1f4f6} Gradual Depth - Five Levels")
$lines.Add("")
$lines.Add("**Level 1 - What it is (anyone can understand):**")
$lines.Add("")
$lines.Add("IAM is the system that decides who can do what in a")
$lines.Add("computer system. It is why you have a username and")
$lines.Add("password, why some buttons are grayed out for some")
$lines.Add("users, and why there is a record when something goes")
$lines.Add("wrong.")
$lines.Add("")
$lines.Add("**Level 2 - How to use it (junior developer):**")
$lines.Add("")
$lines.Add("IAM manifests as: (1) a login system verifying")
$lines.Add("credentials, (2) middleware that checks permissions")
$lines.Add("before serving resources, (3) an audit log recording")
$lines.Add("significant actions. Most web frameworks provide")
$lines.Add("authentication middleware. Authorization is often")
$lines.Add("custom: a user can only modify their own posts; admins")
$lines.Add("can modify all posts.")
$lines.Add("")
$lines.Add("**Level 3 - How it works (mid-level engineer):**")
$lines.Add("")
$lines.Add("IAM works by issuing cryptographically signed tokens")
$lines.Add("after authentication. A JWT contains claims (user ID,")
$lines.Add("roles, expiry) signed by the IdP's private key. Resource")
$lines.Add("servers verify the signature with the public key without")
$lines.Add("calling back to the IdP - the token IS the proof.")
$lines.Add("Authorization policy (RBAC, ABAC) maps token claims to")
$lines.Add("permitted actions. This decoupling enables stateless,")
$lines.Add("distributed authorization.")
$lines.Add("")
$lines.Add("**Level 4 - Why it was designed this way (senior/staff):**")
$lines.Add("")
$lines.Add("The IdP / Resource Server separation enables federation:")
$lines.Add("Organization A can accept tokens from Organization B's")
$lines.Add("IdP without sharing a database. OAuth 2.0 formalized")
$lines.Add("this with the Authorization Server distinct from Resource")
$lines.Add("Server. The critical constraint: the Resource Server must")
$lines.Add("verify token authenticity without trusting content")
$lines.Add("blindly - hence cryptographic signatures. Without")
$lines.Add("signatures, any client could forge claims.")
$lines.Add("")
$lines.Add("**Level 5 - Mastery (distinguished engineer):**")
$lines.Add("")
$lines.Add("IAM is the most latency-sensitive security layer because")
$lines.Add("every request passes through it. Token validation is")
$lines.Add("CPU-bound and must be cached aggressively (public key")
$lines.Add("cache). Authorization policy evaluation at scale (ABAC")
$lines.Add("with complex conditions) requires in-process policy")
$lines.Add("engines (OPA sidecar, Zanzibar-style) - database lookups")
$lines.Add("per request collapse under load. The unsolved production")
$lines.Add("problem: consistent token revocation at scale. A revoked")
$lines.Add("JWT remains valid until expiry unless active revocation")
$lines.Add("check is added - which reintroduces the network hop that")
$lines.Add("stateless tokens were designed to eliminate.")
$lines.Add("")
