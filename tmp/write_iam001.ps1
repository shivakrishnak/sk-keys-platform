$lines.Add("system needs a way to make verified claims portable and")
$lines.Add("unforgeable. Early systems used server-side sessions")
$lines.Add("(session ID in a cookie references server-stored state).")
$lines.Add("Modern systems use signed tokens (JWT, SAML assertions)")
$lines.Add("so the claim can be verified without a server round-trip.")
