$lines.Add("### `u{26a0}`u{fe0f} Common Misconceptions")
$lines.Add("")
$lines.Add("| Misconception | Reality |")
$lines.Add("|:---|:---|")
$lines.Add("| Authentication and authorization are the same thing | Authentication = who you are. Authorization = what you are allowed to do. They are separate decisions that can be delegated to separate systems. OAuth explicitly separates them. |")
$lines.Add("| HTTPS makes IAM secure | HTTPS protects credentials in transit but does nothing about weak passwords, token theft from client storage, or misconfigured authorization policies. |")
$lines.Add("| Admins should have standing access | Standing privileged access is a security liability. Just-in-time (JIT) access grants elevated permissions only when needed and revokes them automatically. |")
$lines.Add("| IAM is only for user-facing systems | Every service-to-service call also needs IAM. An internal microservice without authentication can be exploited by any service that can reach it on the network. |")
