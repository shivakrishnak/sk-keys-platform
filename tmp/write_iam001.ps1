$lines.Add("need: (1) an Identity Provider (IdP) that authenticates")
$lines.Add("and issues verified identity claims; (2) a Resource")
$lines.Add("Server (RS) that accepts those claims and evaluates")
$lines.Add("authorization policy. These can be the same system or")
$lines.Add("separate services.")
$lines.Add("")
$lines.Add("**THE TRADE-OFFS:**")
$lines.Add("")
$lines.Add("**Gain:** fine-grained, auditable, revocable access")
$lines.Add("control at any scale with full accountability.")
$lines.Add("")
$lines.Add("**Cost:** complexity - every action now has multiple")
$lines.Add("layers (authentication, authorization, audit) that must")
$lines.Add("be maintained, synchronized, and secured.")
$lines.Add("")
$lines.Add("**ESSENTIAL vs ACCIDENTAL COMPLEXITY:**")
$lines.Add("")
$lines.Add("**Essential:** In any multi-user system, access")
$lines.Add("differentiation is irreducible. You cannot avoid the")
